package lessons.lesson2.lesson2;

//Блок1 Задача 2:
//Используя while вывести все числа от 0 до 25 в одну строку через пробел.
public class Exercition2 {
    public static void main(String[] args) {
        int number = 0;
        while (number <= 25) {
            System.out.print(number + " ");
            number++;
        }
    }
}
